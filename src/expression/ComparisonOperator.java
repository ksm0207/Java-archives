package expression;

public class ComparisonOperator {
    public static void main(String [] args){

        int x = 10;
        int y = 20;

        System.out.println(x == y); // F
        System.out.println(x != y); // T
        System.out.println(x > y);  // F
        System.out.println(x < y);  // T
        System.out.println(x >= y); // F
        System.out.println(x <= y); // T


    }
}
